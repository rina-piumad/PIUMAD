// 최신순 포트폴리오 가져오기 (id가 높을수록 최신)
export function getLatestPortfolioItems(count: number = 6): PortfolioItem[] {
    return [...allPortfolioItems]
        .sort((a, b) => parseInt(b.id) - parseInt(a.id))
        .slice(0, count);
}

// 카테고리별 포트폴리오 가져오기
export function getPortfolioByCategory(category: string): PortfolioItem[] {
    if (category === 'all') return allPortfolioItems;
    return allPortfolioItems.filter(item => item.category === category);
}
